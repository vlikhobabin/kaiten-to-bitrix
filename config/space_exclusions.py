"""
Конфигурация исключений для миграции пространств.
Список пространств верхнего уровня, которые НЕ переносятся в Bitrix24.
"""

# Пространства которые полностью исключаются из миграции
# (ни само пространство, ни все его дочерние пространства)
EXCLUDED_SPACES = [
    "Удаленные",
    "ТЕСТ Входящие задачи", 
    "Процесс настройки Kaiten",
    "Изменения и автоматизация",
    "Дирекция по персоналу: Заявления",
    "Дирекция по персоналу: Входящие задачи", 
    "Дирекция по персоналу",
    "Личные задачи, тесты"
]

def is_space_excluded(space_title: str) -> bool:
    """
    Проверяет, исключено ли пространство из миграции.
    
    Args:
        space_title: Название пространства
        
    Returns:
        True если пространство исключено
    """
    return space_title.strip() in EXCLUDED_SPACES

def get_excluded_spaces() -> list:
    """Возвращает список исключенных пространств"""
    return EXCLUDED_SPACES.copy() 